def generate_random_password(length, use_uppercase, use_lowercase, use_numbers, use_symbols):
    """Generates a random password based on specified criteria."""
    character_set = ''
    if use_uppercase:
        character_set += string.ascii_uppercase
    if use_lowercase:
        character_set += string.ascii_lowercase
    if use_numbers:
        character_set += string.digits
    if use_symbols:
        character_set += string.punctuation

    if not character_set:
        raise ValueError("At least one character set must be selected.")

    password = ''.join(secrets.choice(character_set) for _ in range(length))
    return password

def generate_memorable_password(num_words, min_length, max_length, num_numbers, num_symbols, capitalization, leet_level):
    """Generates an advanced memorable password."""
    words = WORDLIST
    if not words:
        raise ValueError("The wordlist is empty.")

    if num_words < 1:
        raise ValueError("A memorable password must have at least one word.")

    word_parts = []
    # 1. Word selection and mangling
    for _ in range(num_words):
        word = secrets.choice(words)
        
        # Capitalization
        if capitalization == 'first':
            word = word.capitalize()
        elif capitalization == 'random':
            word = ''.join(c.upper() if secrets.choice([True, False]) else c.lower() for c in word)

        # Leetspeak
        if leet_level != 'none':
            new_word = ''
            for char in word:
                if char.lower() in LEET_SUBSTITUTIONS:
                    if leet_level == 'all' or (leet_level == 'some' and secrets.choice([True, False])):
                        new_word += LEET_SUBSTITUTIONS[char.lower()]
                    else:
                        new_word += char
                else:
                    new_word += char
            word = new_word
        word_parts.append(word)

    # 2. Add numbers and symbols
    other_parts = []
    for _ in range(num_numbers):
        other_parts.append(str(secrets.randbelow(10)))
    for _ in range(num_symbols):
        other_parts.append(secrets.choice(string.punctuation))

    # 3. Shuffle and join
    rng = secrets.SystemRandom()
    
    # Ensure the password starts with a word
    first_word = word_parts.pop(secrets.randbelow(len(word_parts)))
    
    password_parts = word_parts + other_parts
    rng.shuffle(password_parts)
    
    password = first_word + "".join(password_parts)

    # 4. Pad to meet min_length
    if len(password) < min_length:
        padding_needed = min_length - len(password)
        padding_chars = string.ascii_letters + string.digits + string.punctuation
        password += ''.join(secrets.choice(padding_chars) for _ in range(padding_needed))

    # 5. Truncate to meet max_length
    if max_length > 0 and len(password) > max_length:
        password = password[:max_length]

    return password

def main():
    """Main function to parse arguments and generate passwords."""
    parser = argparse.ArgumentParser(
        description="An advanced password generator.",
        formatter_class=argparse.RawTextHelpFormatter
    )
    subparsers = parser.add_subparsers(dest='command', required=True, help='Available commands')

    # Sub-parser for random passwords
    parser_random = subparsers.add_parser('random', help='Generate a completely random password.')
    parser_random.add_argument('-l', '--length', type=int, default=DEFAULT_LENGTH,
                               help=f'Length of the password (default: {DEFAULT_LENGTH}).')
    parser_random.add_argument('--no-uppercase', action='store_false', dest='use_uppercase', help='Exclude uppercase letters.')
    parser_random.add_argument('--no-lowercase', action='store_false', dest='use_lowercase', help='Exclude lowercase letters.')
    parser_random.add_argument('--no-numbers', action='store_false', dest='use_numbers', help='Exclude numbers.')
    parser_random.add_argument('--no-symbols', action='store_false', dest='use_symbols', help='Exclude symbols.')
    parser_random.add_argument('-c', '--count', type=int, default=1, help='Number of passwords to generate (default: 1).')
    parser_random.add_argument('--copy', action='store_true', help='Copy the password to the clipboard (only for single password generation).')
    parser_random.add_argument('--mask', action='store_true', help='Mask the password in the terminal output.')
    parser_random.set_defaults(use_uppercase=True, use_lowercase=True, use_numbers=True, use_symbols=True)

    # Sub-parser for memorable passwords
    parser_memorable = subparsers.add_parser('memorable', help='Generate a memorable, leetspeak-style password.')
    parser_memorable.add_argument('-w', '--num-words', type=int, default=DEFAULT_WORD_COUNT,
                                  help=f'Number of words to combine (default: {DEFAULT_WORD_COUNT}).')
    parser_memorable.add_argument('--min-length', type=int, default=0,
                                  help='Minimum password length. Pads with random characters if needed.')
    parser_memorable.add_argument('--max-length', type=int, default=0,
                                  help='Maximum password length. Truncates the password if needed.')
    parser_memorable.add_argument('-n', '--num-numbers', type=int, default=DEFAULT_NUM_NUMBERS,
                                  help=f'Number of numbers to include (default: {DEFAULT_NUM_NUMBERS}).')
    parser_memorable.add_argument('-S', '--num-symbols', type=int, default=DEFAULT_NUM_SYMBOLS,
                                  help=f'Number of symbols to include (default: {DEFAULT_NUM_SYMBOLS}).')
    parser_memorable.add_argument('-C', '--capitalization', choices=['none', 'first', 'random'], default='first',
                                  help='Capitalization style (default: first).')
    parser_memorable.add_argument('-L', '--leet', choices=['none', 'some', 'all'], default='some',
                                  help='Level of leetspeak substitution (default: some).')
    parser_memorable.add_argument('-c', '--count', type=int, default=1, help='Number of passwords to generate (default: 1).')
    parser_memorable.add_argument('--copy', action='store_true', help='Copy the password to the clipboard (only for single password generation).')
    parser_memorable.add_argument('--mask', action='store_true', help='Mask the password in the terminal output.')

    args = parser.parse_args()

    passwords = []
    try:
        # Determine count based on the sub-command
        count = args.count if hasattr(args, 'count') else 1

        for _ in range(count):
            if args.command == 'random':
                password = generate_random_password(
                    args.length,
                    args.use_uppercase,
                    args.use_lowercase,
                    args.use_numbers,
                    args.use_symbols
                )
            elif args.command == 'memorable':
                password = generate_memorable_password(
                    args.num_words,
                    args.min_length,
                    args.max_length,
                    args.num_numbers,
                    args.num_symbols,
                    args.capitalization,
                    args.leet
                )
            passwords.append(password)
    except (ValueError, FileNotFoundError) as e:
        print(f"Error: {e}")
        return

    # Handle copying
    copied = False
    if args.copy:
        if len(passwords) == 1:
            try:
                import pyperclip
                pyperclip.copy(passwords[0])
                copied = True
            except ImportError:
                print("Error: To use the --copy feature, please install the pyperclip library:")
                print("pip install pyperclip")
            except Exception as e:
                print(f"Error: Could not copy to clipboard. {e}")
        else:
            print("Note: --copy is only available when generating a single password.")

    # Handle output
    if args.mask:
        if copied:
            print("Password generated and copied to clipboard.")
        else:
            print("Password(s) generated.")
    else:
        for password in passwords:
            print(password)
        if copied:
            print("\n(Password copied to clipboard)")

if __name__ == "__main__":
    main()
